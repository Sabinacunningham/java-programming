package day14_multi_branch_if_statements;

public class DayActivity {
    public static void main(String[] args) {
        String weather = "snowy";
        if (weather.equals("sunny")) {
            System.out.println("Go to park, hiding and code java");
        }else if (weather.equals("rainy")) {
            System.out.println("stay home, drink tea and code java");
        }else if (weather.equals("snowy")) {
            System.out.println("Drink hot chocolate, build snowman ");
        }else if (weather.equals("windy")) {
            System.out.println("Get ready for power loss");
        }else {
            System.out.println("Keep coding java");
        }
        }


        }



